
public class Data {
	private String configuration;
    private int score; 
    
	
    // constructor method
	public Data(String config, int score) {
        this.configuration = config;
        this.score = score;
	} // method Data
	
	
	// getter method
	public String getConfiguration() {
		return configuration;
	} // method getConfiguration
	
	
	// getter method
	public int getScore() {
		return score;
	} // method getScore
	
	
	
} // class ClassData
